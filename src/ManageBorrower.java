    private void borrowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrowActionPerformed
        // TODO add your handling code here:
        
        String query = "INSERT INTO `borrow`(`studentID`, `name`, `faculty`, `department`, `accessionNO`, `isbn`, `date`) VALUES (?,?,?,?,?,?,?)";
        if(sid.getText().equals("")||name.getText().equals("")||faculty.getText().equals("")||department.getText().equals("")||an.getText().equals("")||isbn.getText().equals("")){
            borrow.setEnabled(false);
            JOptionPane.showMessageDialog(jPanel4, "Borrow Failed! Please check your Fields", "Error", 1);
        }else{
            borrow.setEnabled(true);
            try {
                Connection con = Connectionz.getConnection();
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, sid.getText());
                pst.setString(2, name.getText());
                pst.setString(3, faculty.getText());
                pst.setString(4, department.getText());
                pst.setString(5, an.getText());
                pst.setString(6, isbn.getText());

                Date d = new Date();
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                pst.setString(7, sdf.format(d));
                pst.execute();

                JOptionPane.showMessageDialog(panel1,"Book Borrowed");

                JTextField temp = null;
                for(Component c:panel1.getComponents()){
                    if(c.getClass().toString().contains("javax.swing.JTextField")){
                        temp = (JTextField)c;
                        temp.setText(null);
                    }
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_borrowActionPerformed


