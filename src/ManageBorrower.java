    private void sidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sidKeyReleased
        // TODO add your handling code here:
           
        String query = "select * from students where studentID=?";
        if(sid.getText().equals("")||name.getText().equals("")||faculty.getText().equals("")||department.getText().equals("")||an.getText().equals("")||isbn.getText().equals("")){
            borrow.setEnabled(false);
        }
        try{
            Connection con = Connectionz.getConnection();
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, sid.getText());
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                name.setText(rs.getString("name"));
                faculty.setText(rs.getString("faculty"));
                department.setText(rs.getString("department"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_sidKeyReleased

